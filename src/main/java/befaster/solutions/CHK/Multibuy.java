package befaster.solutions.CHK;

public class Multibuy {
    private final Integer count;
    private final Integer price;

    public Multibuy(Integer count, Integer value) {
        this.count = count;
        this.price = value;
    }

    public Integer getCount() {
        return count;
    }

    public Integer getPrice() {
        return price;
    }
}

